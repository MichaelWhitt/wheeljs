function wheelTwo(){
    let s = document.getElementById("s1");
    let d1 = document.createElement("div");
    let d2 = document.createElement("div");
    let d3 = document.createElement("div");
    let d4 = document.createElement("div");
    let sp1 = document.createElement("span");
    let sp2 = document.createElement("span");
    s.appendChild(d1);
    d1.appendChild(d2);
    d2.appendChild(d3);
    d3.appendChild(d4);
    d4.appendChild(sp1);
    d4.appendChild(sp2);
    d1.id = "d1";
    d1.className = "d1";
    d2.id = "d2";
    d2.className = "d2";
    d3.id = "d3";
    d3.className = "d3";
    d4.id = "d4";
    d4.className = "d4";

   
    sp1.classList.toggle("span1w2");
    sp2.classList.toggle("span2w2");

}

